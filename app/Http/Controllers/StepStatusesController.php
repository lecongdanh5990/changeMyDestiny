    public function index(Step $step){
        $listOfStatuses=$step->statuses()
                ->get(['id','content','tomato','rating','created_at']);
        foreach($listOfStatuses as $status ){
            $status['formatDate']=Carbon::createFromFormat('Y-m-d H:i:s', $status['created_at'])->format('d-m-Y');
        }
        return $listOfStatuses;
    }

